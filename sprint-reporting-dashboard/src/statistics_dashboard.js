// =================================================================
// Utility helpers
// =================================================================

function normalizeHeaders_(headers) {
  return headers.map(header =>
    normalizeHeader_(header)
  );
}

function normalizeHeader_(header) {
  return String(header)
    .toLowerCase()
    .replace(/[\s\u00A0]+/g, ' ')
    .trim();
}
